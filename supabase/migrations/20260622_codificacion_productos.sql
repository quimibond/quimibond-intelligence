-- Decodificador de claves de TELA (PD0201) usando el diccionario
CREATE OR REPLACE FUNCTION decodificar_tela(p_ref text)
RETURNS TABLE(
  ref text, en_kg boolean, fibra text, dibujo text,
  peso_gm2 text, color text, acabado text
) LANGUAGE sql STABLE AS $$
  WITH c AS (
    SELECT regexp_replace(upper(trim(p_ref)),'^I','') AS s,
           upper(trim(p_ref)) LIKE 'I%' AS kg
  ),
  d AS (
    SELECT s, kg, substring(s from '[A-Z]([A-Z]{2})[0-9]{2,3}') AS color_cl FROM c
  )
  SELECT
    p_ref, d.kg,
    (SELECT descripcion FROM codificacion_claves WHERE documento='PD0201' AND eje='fibra'  AND clave=substring(d.s,1,1)),
    (SELECT descripcion FROM codificacion_claves WHERE documento='PD0201' AND eje='dibujo' AND clave=substring(d.s,2,1)),
    substring(d.s from '^.{2}([0-9]{2,3})'),
    (SELECT descripcion FROM codificacion_claves WHERE documento='PD0201' AND eje='color'   AND clave=d.color_cl),
    (SELECT descripcion FROM codificacion_claves WHERE documento='PD0201' AND eje='acabado' AND clave=right(d.s,2) AND right(d.s,2) ~ '^[A-Z]{2}$')
  FROM d;
$$;
