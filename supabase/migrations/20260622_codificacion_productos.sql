-- Codificación oficial de productos Quimibond (DAT PD0201-04)
CREATE TABLE IF NOT EXISTS codificacion_documentos (
  codigo text PRIMARY KEY,
  nombre text NOT NULL,
  area text,
  revision text,
  vigencia date
);

CREATE TABLE IF NOT EXISTS codificacion_claves (
  id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  documento text NOT NULL REFERENCES codificacion_documentos(codigo),
  eje text NOT NULL,          -- fibra, dibujo, color, acabado, operacion, tela_base, ...
  posicion text,              -- posición(es) en la clave
  clave text NOT NULL,        -- el código (W, J, BL, ...)
  descripcion text NOT NULL,  -- significado
  UNIQUE(documento, eje, clave)
);
CREATE INDEX IF NOT EXISTS idx_codif_claves_doc_eje ON codificacion_claves(documento, eje);
CREATE INDEX IF NOT EXISTS idx_codif_claves_clave ON codificacion_claves(eje, clave);

